package com.dev.model;

public class ReviewVo {
	private String review;
	private String saying_number;
	private String id;
	private String saying;
	private String person;
	
	
	
	
	public String getSaying() {
		return saying;
	}
	public void setSaying(String saying) {
		this.saying = saying;
	}
	public String getPerson() {
		return person;
	}
	public void setPerson(String person) {
		this.person = person;
	}
	public String getReview() {
		return review;
	}
	public void setReview(String review) {
		this.review = review;
	}
	public String getSaying_number() {
		return saying_number;
	}
	public void setSaying_number(String saying_number) {
		this.saying_number = saying_number;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	
	
	
}
